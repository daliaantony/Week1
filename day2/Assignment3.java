package week1.day2;

public class Assignment3 {

public static void main(String[] args) {
		
int input = 5;
int fact = 1;
for (int i = 1; i <= input; i++) {
	
	fact = fact * i;
    }
    System.out.println("The factorial of "+input+" is "+fact);
  }

}

