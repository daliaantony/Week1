package week1.day2;

public class SampleIfCondition {

	public static void main(String[] args) {
		
		int mark = 360;
		
		if(mark > 400)
			System.out.println("Given mark is greater than 400");
		else if(mark > 320)
			System.out.println("Given mark is greater than 320");
		else
			System.out.println("Given mark is less than 320");
	}
}
